#ifndef POINT_HPP
#define POINT_HPP

#include <cmath>

struct Point {
    double x, y;

    Point() : x(0), y(0) {}
    Point(double x, double y) : x(x), y(y) {}

    bool operator<(const Point& other) const {
        return (x != other.x) ? x < other.x : y < other.y;
    }

    bool operator==(const Point& other) const {
        return std::abs(x - other.x) < 1e-9 && std::abs(y - other.y) < 1e-9;
    }
};

#endif
